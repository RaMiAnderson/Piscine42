#include <unistd.h>

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		i++;
	}
}

int	main(int argC, char *argV[])
{
	if (argC)
		ft_putstr(argV[0]);
	write(1, "\n", 1);
	return (0);
}
